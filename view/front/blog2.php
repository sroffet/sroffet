        <!-- Post Content -->
        <p class="lead">LSpéculateur glacé ou accumulateur compulsif, découvreur de talents génial ou mécène providentiel : le Collectionneur d’art contemporain ne laisse personne indifférent ! Mais qui est-il vraiment ? Le bazar culturel dresse un portrait de 6 profils de Collectionneurs aussi passionnants les uns que les autres. Et vous, quel type de Collectionneur d’Art êtes-vous ?</p>

        <p class="partie2"><strong>1# Le collectionneur d’art « Chercheur d’or »</strong></p>
        <p>On prête au Chercheur d’or des moyens colossaux. Ce type de collectionneur aurait d’ailleurs la responsabilité de l’inflation galopante d’un Art contemporain qui enflamme (jusqu’à l’absurde parfois) les salles des ventes internationales.</p>
        <br>
        <img class="img-fluid rounded" src="images/blog2(1).jpg" alt="">
      </br>
        <p>Pour Cyril Mercier*, auteur d’une thèse sur les Collectionneurs d’Art contemporain, certains fonctionnent en effet sur un mode spéculatif :
          « Je peux revendre des œuvres, soit par spéculation, soit par désintérêt pour l’œuvre. […] Il peut, en effet, y avoir une démarche spéculative. Il peut m’arriver d’acheter à New York et de revendre à Paris. L’art contemporain répond à un contexte social. Il ne faut donc pas voir l’art spéculatif comme péjoratif. Il reflète notre société actuelle. Il est donc très important. »
          Cyril Mercier conclut cependant que l’aspect spéculatif découle d’une loi de marché incontournable à laquelle sont soumis les collectionneurs. Toutefois, le gain financier ne semble pas jouer un rôle exclusif dans leur engagement vis-à-vis de l’Art contemporain.          
          </p>
        
          <p class="partie2"><strong>2# Le Chasseur</strong></p>
          <br>
        <img class="img-fluid rounded" src="images/blog2(2).jpg" alt="">
        </br>
        <p>Ce type de Collectionneur d’art passe son temps à « traquer » le beau et l’insolite. Il « cherche la perfection par l’harmonie et la beauté. » (Maurice Rheims, La vie étrange des objets, 1959).
          Mais surtout d’après Cyril Mercier, les premières raisons pour expliquer le besoin de collection s’inscrivent principalement dans le sentiment de plaisir. Et ils sont nombreux : plaisir d’acheter une première œuvre, plaisir de posséder, ou encore simple plaisir de contempler une œuvre.</p>
        
          <p class="partie2"><strong>3# L’Aventurier</strong></p>
          <br>
        <img class="img-fluid rounded" src="images/blog2(3).jpg" alt="">
        </br>
        <p>Pour d’autres encore le fait de collectionner relève de l’aventure :
          « Pour être dans l’art contemporain, il faut un esprit d’aventure », « l’art est le dernier secteur d’aventure humaine et intellectuelle. » avancent des collectionneurs interrogés qui se considèrent comme de vrais aventuriers au goût du risque prononcé ! L’Art ne serait donc pas qu’un long fleuve tranquille…</p>
        
          <p class="partie2"><strong>4# L’Amoureux</strong></p>
          <br>
        <img class="img-fluid rounded" src="images/blog2(4).jpg" alt="">
        </br>
        <p>Autre archétype de Collectionneur d’art : les véritables amoureux, pour lesquels passion de l’Art et valeurs humaines sont interdépendantes.
          Pour certains collectionneurs, « le but de l’Art contemporain, c’est la découverte de ce qui se passe et aussi l’ouverture de l’œil, la tolérance. » et d’ajouter qu’ils sont « obligés de regarder dans tous les sens pour être bousculé. »
          D’autres voient l’art comme « une transgression » où « au bout du chemin, on devient un homme libre. </p>
          
          <p class="partie2"><strong>5# Le Spirituel</strong></p>
          <br>
          <img class="img-fluid rounded" src="images/blog2(5).jpg" alt="">
        </br>
          <p>L’acquisition d’œuvres d’art se rapproche pour certains de la quête spirituelle et interroge le devenir civilisationnel.
            Le regard de l’Art contemporain permet de développer sa capacité à regarder le monde. Il amène à chercher le sens profond de chaque chose. À chaque époque et à chaque contexte, l’Art a toujours été un précurseur de la culture et de la civilisation.
            Certains ont acquis dans leur collection leur individualité et leur indépendance :
            « L’Art est la quête d’un Graal. J’ai dû choisir un métier pour plaire à mon père. L’Art m’a permis de nourrir ma trajectoire, de disposer d’une soupape de sécurité. Enfin quelque chose qui n’appartient qu’à moi. » déclare un collectionneur interrogé par Cyril Mercier. </p>
            
          <p class="partie2"><strong>6# Le collectionneur d’art… discret</strong></p>
          
        <p>Quels que soient son profil ou sa spécificité, il semble globalement que le cercle des collectionneurs d’Art contemporain demeure hermétiquement fermé. </p>
       <br>
        <img class="img-fluid rounded" src="images/blog2(6).jpg" alt="">
      </br>
        <p>« En effet, le milieu de l’Art moderne français est un petit milieu dont les acteurs se connaissent très bien entre eux, qu’ils soient collectionneurs, marchands, experts ou conservateurs. Tous construisent leurs relations sur des bases similaires : le culte du secret, la relation courtoise et de cooptation, le fonctionnement en réseau » souligne Cyril Mercier.
          Le collectionneur reste discret et fidèle à la tradition française ne souhaite pas afficher ses avoirs : « En France, on ne dit pas son nom, on cache ses trésors. Sur une centaine de possesseurs d’œuvres assez remarquables pour être régulièrement demandées par les musées, trois, peut-être quatre, acceptent que soit dévoilée leur identité » (Mona Thomas Un art du secret : collectionneurs d’art contemporain en France).
          </p>
         <p> L’amateur, étymologiquement « celui qui aime », n’est peut être pas (encore) un collectionneur mais il contribue lui aussi à la pérennité de l’Art. Et vous, quel type de Collectionneur êtes-vous ?</p>
